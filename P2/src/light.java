
public class light {
    double x;
    double y;
    double z;
    double w;
    double red;
    double green;
    double blue;

    public light(double x, double y, double z, double w, double red, double blue, double green){
        this.x = x;
        this.y = x;
        this.z = z;
        this.w = w;
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
}
