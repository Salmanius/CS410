
public class mySphere {
    double cx;
    double cy;
    double cz;
    double radius;

    public mySphere(double cx, double cy, double cz, double radius){
        this.cx = cx;
        this.cy = cy;
        this.cz = cz;
        this.radius = radius;
    }

    public double getCx() {
        return cx;
    }

    public double getCy() {
        return cy;
    }

    public double getCz() {
        return cz;
    }

    public double getRadius() {
        return radius;
    }
}
